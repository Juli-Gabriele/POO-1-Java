public class CalculadoraSimples {
    
    
    public float somar (float nr1, float nr2){
        return nr1 + nr2;
    
}
    public float subtrair (float nr1, float nr2){
        return nr1 - nr2;
    
}
    public float multiplicar (float nr1, float nr2){
        return nr1 * nr2;
    
}
    public float dividir (float nr1, float nr2){
        return nr1 / nr2;
    
}
}

    
   
